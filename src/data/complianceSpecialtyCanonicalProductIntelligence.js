/** @param {ComplianceSpecialtyCanonicalProductIntelligence} row */
function freezeComplianceRow(row) {
  const listKeys = [
    "aliases",
    "routingKeywords",
    "applications",
    "specifications",
    "approvals",
    "registrations",
    "differentiators",
    "cautions",
    "bestFit",
    "notBestFit",
    "productNumbers",
    "customerProfileSignals",
    "customerProfileQuestions",
    "salesEnablementAngles",
    "productSpotlightAngles",
    "categorySpotlightAngles",
    "crossSellSignals",
    "operationalPainPoints",
    "complianceTriggers",
    "environmentalRiskSignals",
    "sourceNotes",
  ];
  /** @type {Record<string, unknown>} */
  const frozen = { ...row };
  for (const key of listKeys) {
    const val = row[key];
    frozen[key] = Object.freeze([...(Array.isArray(val) ? val : [])]);
  }
  return Object.freeze(frozen);
}

