/** @param {IndustrialSpecialtyOilCanonicalProductIntelligence} row */
function freezeSpecialtyRow(row) {
  const listKeys = [
    "aliases",
    "routingKeywords",
    "applications",
    "specifications",
    "approvals",
    "differentiators",
    "cautions",
    "bestFit",
    "notBestFit",
    "productNumbers",
    "salesPositioning",
    "customerProfileSignals",
    "customerProfileQuestions",
    "salesEnablementAngles",
    "productSpotlightAngles",
    "categorySpotlightAngles",
    "crossSellSignals",
    "operationalPainPoints",
    "sourceNotes",
  ];
  /** @type {Record<string, unknown>} */
  const frozen = { ...row };
  for (const key of listKeys) {
    const val = row[key];
    frozen[key] = Object.freeze([...(Array.isArray(val) ? val : [])]);
  }
  return Object.freeze(frozen);
}

